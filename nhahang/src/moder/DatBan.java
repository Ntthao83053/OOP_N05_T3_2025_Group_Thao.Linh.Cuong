public class DatBan {
    String tenKhachHang;
    int soNguoi;
    String gioDat;
    int soBan;
    String ngayDat;

    
    public DatBan(String tenKhachHang, int soNguoi, String gioDat, int soBan, String ngayDat) {
        this.tenKhachHang = tenKhachHang;
        this.soNguoi = soNguoi;
        this.gioDat = gioDat;
        this.soBan = soBan;
        this.ngayDat = ngayDat;
    }
}
